#include "libft.h"

int		ft_isprime(int n)
{
	int i;

	if (n < 2)
		return (0);
	i = 2;
	while ((i * i) <= n)
	{
		if (n % i == 0)
			return (0);
		i++;
	}
	return (1);
}
